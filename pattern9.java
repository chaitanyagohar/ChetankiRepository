public class pattern9 {
    public static void main(String[] args) {
        int n=5;
        for(int i=1;i<=5;i++){
            int spc=n-i;
            for(int j=1;j<=spc;j++){
                System.out.print(" ");
            }
            for(int j=1;j<=5;j++){
                System.out.print("*");
            }
            System.out.println();

        }
    }
}


